package p3;
public class ShortHandRule_DoWhileLoop 
{
    public static void main(String[] args)
    {
        int sid[]={111,112,113,114,115,116,117,118,119};
        int i=0;
        do
        {
            System.out.println(sid[i]);
            i++;
        }
        while(i<sid.length);
    }
 
}
